type subwayLookup struct {
	score float64
	place int
}

var subwayIndex map[string]subwayLookup

func init() {
	subwayIndex = make(map[string]subwayLookup, len(subways))
	for i, s := range subways {
		subwayIndex[s.name] = subwayLookup{score: s.score, place: i + 1}
	}
}

const (
	undefinedScore              = 1001.0
	undefinedPlace              = 1001
	additionalStationMultiplier = 2.0
)

type undergroundStation struct {
	Name       string `json:"name"`
	TravelTime int    `json:"travelTime"`
	TravelType string `json:"travelType"`
}

// ParseUndergroundInfo computes the best underground score, station place, and distance description
// from a slice of raw JSON station objects returned by the CIAN API.
func ParseUndergroundInfo(stationsRaw []json.RawMessage) (score float64, place int, distInfo string) {
	bestScore := undefinedScore
	bestPlace := undefinedPlace
	var info string
	count := 0

	for _, raw := range stationsRaw {
		var station undergroundStation
		if err := json.Unmarshal(raw, &station); err != nil {
			continue
		}
		count++

		stationScore := undefinedScore
		stationPlace := undefinedPlace
		if sub, ok := subwayIndex[station.Name]; ok {
			stationScore = sub.score
			stationPlace = sub.place
		}

		if station.TravelType == "walk" {
			stationScore += 1.1 * float64(station.TravelTime)
		} else {
			stationScore += 7.0 * float64(station.TravelTime)
		}

		if stationScore < bestScore {
			bestScore = stationScore
			bestPlace = stationPlace
		}

		info += fmt.Sprintf("%s: %d мин. ", station.Name, station.TravelTime)
		if station.TravelType == "walk" {
			info += "пешком, "
		} else {
			info += "на транспорте, "
		}
	}

	if count > 1 {
		bestScore -= float64(count-1) * additionalStationMultiplier
	}

	return bestScore, bestPlace, info
}
